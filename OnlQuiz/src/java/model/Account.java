package model;

/**
 *
 * @author dinht
 */
public class Account {

    private int id;
    private String userName;
    private String passWord;
    private int type;
    private String email;

    public Account(int id, String userName, String passWord, int type, String email) {
        this.id = id;
        this.userName = userName;
        this.passWord = passWord;
        this.type = type;
        this.email = email;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getPassWord() {
        return passWord;
    }

    public void setPassWord(String passWord) {
        this.passWord = passWord;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public String toString() {
        return "Account{" + "id=" + id + ", userName=" + userName + ", passWord=" + passWord + ", type=" + type + ", email=" + email + '}';
    }

}
